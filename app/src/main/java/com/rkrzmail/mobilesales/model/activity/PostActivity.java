
package com.rkrzmail.mobilesales.model.activity;

import java.io.Serializable;
import android.os.Parcel;
import android.os.Parcelable;
import android.os.Parcelable.Creator;
import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

public class PostActivity implements Serializable, Parcelable
{

    @SerializedName("status")
    @Expose
    private Boolean status;
    @SerializedName("message")
    @Expose
    private String message;
    public final static Creator<PostActivity> CREATOR = new Creator<PostActivity>() {


        @SuppressWarnings({
            "unchecked"
        })
        public PostActivity createFromParcel(Parcel in) {
            return new PostActivity(in);
        }

        public PostActivity[] newArray(int size) {
            return (new PostActivity[size]);
        }

    }
    ;
    private final static long serialVersionUID = 5880193300032161274L;

    protected PostActivity(Parcel in) {
        this.status = ((Boolean) in.readValue((Boolean.class.getClassLoader())));
        this.message = ((String) in.readValue((String.class.getClassLoader())));
    }

    public PostActivity() {
    }

    public Boolean getStatus() {
        return status;
    }

    public void setStatus(Boolean status) {
        this.status = status;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public void writeToParcel(Parcel dest, int flags) {
        dest.writeValue(status);
        dest.writeValue(message);
    }

    public int describeContents() {
        return  0;
    }

}
